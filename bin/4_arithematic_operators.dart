void main() {
  // arithematic operators
  int a = 20;
  int b = 10;

  // add operator
  print("Add a + b ${a + b}");

  // subtract operator
  print("Subtract a - b ${a - b}");

  // multiply operator
  print("Multiply a * b ${a * b}");

  // divide operator
  print("Divide a / b ${a / b}");

  // remainder operator
  print("Remainder a%b ${a % b}");

  // integert division operator
  print("Integer division a ~/ b ${a ~/ b}");

  // increment
  print("Increment a : ${++a}");

  // decrement
  print("Decrement a : ${--a}");
}
